import math
# math.ceil(x) CONVERTS FLOAT VALUUES TO NEAREST VALUE(SUCCEED)
x=11.9
print (math.ceil(x))

#math.fabs(x)  RETURNS POSITIVE VALUE
y=-10
print(math.fabs(y))

#math.factorial(x)  RETURNS FACTORIAL OF x
z=3
print(math.factorial(z))

#math.floor(x)   RETURNS THE FLOOR OF X ,THE LARGEST INTEGER LESSTHAN OR EQUAL TO X
a=10.8
print(math.floor(a))

#math.fsum()RETURNS THE SUM OF VALUES
l=[10,20,30]
print(math.fsum(l))

#math.sqrt()  RETURNS SQRT
c=16
print(math.sqrt(c))

